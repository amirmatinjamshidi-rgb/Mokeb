/** همه شهرهای ایران — مرتب‌شده برای dropdown */
export const ALL_IRAN_CITIES: readonly string[] = Object.values(
  IRAN_CITIES_BY_PROVINCE,
)
  .flat()
  .sort((a, b) => a.localeCompare(b, "fa"));

export function getCitiesForProvince(province: string): readonly string[] {
  if (!province) return ALL_IRAN_CITIES;
  return (
    IRAN_CITIES_BY_PROVINCE[province as IranProvince] ?? ALL_IRAN_CITIES
  );
}
